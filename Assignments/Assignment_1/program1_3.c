#include <stdio.h>

///////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : It is used to display numbers from 5 to 1 in decreasing order
//  Input         : None
//  Output        : None(Void)
//  Author        : Avadhoot Subhash Pawar
//  Date          : 27/10/2025
//
///////////////////////////////////////////////////////////////

int Display()
{
    int iNum = 0;
    iNum = 5;

    while (iNum >= 1)                       // Business Logic
    {
        printf("%d\n", iNum);

        iNum--;
    }

}   // End of Display

///////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;

}   // End of main

