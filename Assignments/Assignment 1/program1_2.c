void Display()
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= 5; iCnt++)                // Business Logic
    {
        printf("Marvellous\n");
    }

}   // End of Display

///////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;

}   // End of main

